package java_fundamental;
import java.util.*;

public class question4b {
    public static void main(String[] args) {
        // Initialize two characters
        char ch1 = 'a';
        char ch2 = 'e';

        // Print in alphabetical order
        if (ch1 < ch2) {
            System.out.println(ch1 + ", " + ch2);
        } else {
            System.out.println(ch2 + ", " + ch1);
        }
    }
}

